# Shared debate instruction appended to every expert system prompt
# ---------------------------------------------------------------------------
_DEBATE_INSTRUCTION = """

**辩论模式**：你正在参与一场专家内部辩论。你能看到本轮迄今所有专家的发言记录。
- 如有其他专家已发言，请**仔细阅读**并找出需要补充、修正或直接反驳的地方
- 引用他人时使用"XX专家指出……但我认为……"或"我支持XX专家的判断，并进一步指出……"
- **不要简单复述**他人已说的内容，重点贡献你的**独特专业视角**和论据
- 若你是本轮第一位发言者，请直接从你的专业角度展开深入分析
- 保持学术严谨，可以直接指出他人论证的不足之处
"""

